    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        if(evt.getClickCount() == 4){

            if(c_contador >= 30){
                lb_dialogo.setText(Avisos.TEXTO_AGUARDE_PROCESSO);
                lb_dialogo.setForeground(Color.WHITE); 
                s_telaAguardando.setVisible(false); 
                c_contador = 0;
                threadTempoTarefa.stop();
            }else{
                MostrarDados.s_MostrarDados.mostraMensagemAtualizacao(Avisos.TEXTO_AVISO_FINALIZAR_AGUAR, "aviso");
            }
        }
    }//GEN-LAST:event_jLabel1MouseClicked


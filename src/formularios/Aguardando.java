    private void threadTempoTarefaOnTime(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_threadTempoTarefaOnTime
        c_contador++;

        System.err.println(c_contador);

        if (c_contador <= 15) {

            lb_dialogo.setText(Avisos.TEXTO_AGUARDE_PROCESSO);
            lb_dialogo.setForeground(Color.WHITE);

        }

        if (c_contador > 15 && c_contador <= 30) {

            lb_dialogo.setText(Avisos.TEXTO_AGUARDE_PROCESSO);
            lb_dialogo.setForeground(Color.GREEN);

        }

        if (c_contador > 30 && c_contador <= 60) {

            lb_dialogo.setText(Avisos.TEXTO_PASSOU_30_SEG);
            lb_dialogo.setForeground(Color.CYAN);

        }

        if (c_contador > 60 && c_contador <= 90) {

            //mostra aviso de ddemora mais q o normal...
            lb_dialogo.setText(Avisos.TEXTO_DEMORANDO_ANORMAL);
            lb_dialogo.setForeground(Color.YELLOW);
        }

        if (c_contador > 90) {
            lb_dialogo.setText(Avisos.TEXTO_TEMPO_ESGOTADO);
            lb_dialogo.setForeground(Color.RED);
        }


    }//GEN-LAST:event_threadTempoTarefaOnTime


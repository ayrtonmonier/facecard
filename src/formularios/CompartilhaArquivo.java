private void tf_arquivoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_arquivoKeyReleased
        if(!SolicitarDados.s_SolicitarDados.verificaCaracteresInvalidosNomeArquivo(tf_arquivo.getText())){
            tf_arquivo.setText(tf_arquivo.getText().substring(0, (tf_arquivo.getText().length() -1)));
            String l_msg = Avisos.MSG_ARQ_CARACT_INV;
            MostrarDados.s_MostrarDados.mostraMensagemAtualizacao(l_msg, "aviso");
            tf_arquivo.requestFocus();
        }else{

            tf_arquivo.setText(tf_arquivo.getText().toUpperCase());
                if(tf_arquivo.getText().length() > 50){
                    MostrarDados.s_MostrarDados.mostraMensagemAtualizacao(Avisos.MSG_EXCESSO_CARACT_50, "aviso");
                    tf_arquivo.setText(tf_arquivo.getText().substring(0, 50));
                }
        }
}//GEN-LAST:event_tf_arquivoKeyReleased


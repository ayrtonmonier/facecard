    private void bt_selecionarArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_selecionarArquivoActionPerformed

        String l_diretorio;
        String l_extensao = null;
        String l_tipoArquivo = null;
        String l_iconeArquivo = null;
        String l_tamanho = null;
        
        boolean l_selecaoOk = false;
        double l_tamanhoMb = 0;

        File l_arquivoSelecionado = null;

        fc_selecionadorArquivo = new JFileChooser();
            
            if(!tf_diretorio.getText().isEmpty()){
                fc_selecionadorArquivo.setCurrentDirectory(new File(tf_diretorio.getText())); 
            }

        do{

           int res = fc_selecionadorArquivo.showOpenDialog(null);

           fc_selecionadorArquivo.setDialogTitle(Avisos.TIT_JAN_ESC_ARQ);

                if(res == JFileChooser.APPROVE_OPTION){

                    //somente até 50 mb
                    if(fc_selecionadorArquivo.getSelectedFile().length() <= 50000000){

                        l_selecaoOk = true;

                        //PEGA DADOS DO ARQUIVO
                        l_arquivoSelecionado = fc_selecionadorArquivo.getSelectedFile();

                        //NOME COM EXTENSAO
                        c_nomeExtensaoArquivoSelecionado = l_arquivoSelecionado.getName();

                        //DIRETÓRIO
                        l_diretorio = l_arquivoSelecionado.getParent();

                        //TAMANHO
                        l_tamanho = String.valueOf(l_arquivoSelecionado.length());

                        l_tamanhoMb = (Integer.parseInt(l_tamanho) / 1024) / 1024;

                        //EXTENSAO
                        l_extensao = SolicitarDados.s_SolicitarDados.pegaExtensaoOuNomeArquivo(c_nomeExtensaoArquivoSelecionado, "extensao");

        //                c_bytesArquivo = new byte[(int) l_arquivoSelecionado.length()];
        //
        //                c_bytesArquivo = SolicitarDados.s_SolicitarDados.pegaBytesArquivo(l_arquivoSelecionado);

                        //SETA DADOS NA TELA

                        //SETA NOME
                        tf_arquivo.setText("");
                        tf_arquivo.setEditable(true);
                        tf_arquivo.requestFocus();


                        //SETA ICONE EXTENSAO ARQUIVO
                        l_iconeArquivo = SolicitarDados.s_SolicitarDados.pegaIconeOuTipoExtensao(l_extensao, "icone");
                        lb_iconeTipoArquivo.setIcon(new javax.swing.ImageIcon(getClass().getResource(l_iconeArquivo))); // NOI18N

                        //SETA TIPO ARQUIVO
                        l_tipoArquivo = SolicitarDados.s_SolicitarDados.pegaIconeOuTipoExtensao(l_extensao, "tipo");
                        lb_arquivoTipo.setText(Avisos.TEXTO_DE_UM_NOME_PESSOAL_AO_ARQ);

                        String l_nomeArquivo50carac = SolicitarDados.s_SolicitarDados.trataNomeArquivoPara50caracteres(c_nomeExtensaoArquivoSelecionado);

                        //SETA O NOME ORIGINAL DO ARQUIVO
                        lb_nomeOriginal.setText("("+l_tipoArquivo+") "+l_nomeArquivo50carac);

                        //SETA DIRETÓRIO (LOCAL DO ARQUIVO)
                        tf_diretorio.setText(l_diretorio);

                        //EXTENSÃO
                        tf_extensao.setText(l_extensao);

                        //TAMANHO BYTES
                        tf_tamanho.setText(l_tamanho);

                        //TAMANHO MB
                        lb_tamanhoMb.setText(l_tamanhoMb+" MB");

                        //CONFIRMAR NOME DO ARQUIVO
                        bt_compartilhar.setEnabled(false);
                        bt_confirmarRenomear.setEnabled(true);
                        bt_editarCampo.setEnabled(false);

                    }else{
                        MostrarDados.s_MostrarDados.mostraMensagemAtualizacao(Avisos.MSG_ARQ_MUITO_GRANDE, "erro");
                        l_selecaoOk = false;
                    }


                }else if(res == JFileChooser.CANCEL_OPTION){
                    if(tf_diretorio.getText().isEmpty())
                        MostrarDados.s_MostrarDados.mostraMensagemAtualizacao(Avisos.MSG_ARQ_NAO_SELEC, "aviso");
                    
                    l_selecaoOk = true;
                }

        }while (!l_selecaoOk);
    }//GEN-LAST:event_bt_selecionarArquivoActionPerformed

    private void bt_confirmarRenomearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_confirmarRenomearActionPerformed

        if(tf_arquivo.getText().isEmpty()){

            MostrarDados.s_MostrarDados.mostraMensagemAtualizacao(Avisos.MSG_ARQ_INFORME_NOME, "aviso");
            tf_arquivo.requestFocus();

        }else{


            boolean l_nomeOk = false;
            String l_novoNome = tf_arquivo.getText();
            String l_extensao = tf_extensao.getText();
            String l_nomeCompletoArq = l_novoNome+l_extensao;


                if(SolicitarDados.s_SolicitarDados.nomeArquivoJaFoiUsado(l_nomeCompletoArq)){

                    l_nomeOk = SolicitarDados.s_SolicitarDados.verificaCaracteresInvalidosNomeArquivo(l_novoNome);

                        if(l_nomeOk){

                            //CONFIRMAR NOME DO ARQUIVO
                            c_novoNome = l_novoNome+l_extensao;
                            tf_arquivo.setEditable(false);
                            bt_confirmarRenomear.setEnabled(false);
                            bt_editarCampo.setEnabled(true);
                            
                                
                                if(rb_unicoAmigo.isSelected() || rb_amigosGeral.isSelected() || rb_todosGeral.isSelected()){
                                    
                                    if(rb_unicoAmigo.isSelected() && cb_listaAmigos.isEnabled()){
                                        lb_arquivoTipo.setText(Avisos.TEXTO_SELECIONE_ALGUEM_ESPECIFICO);
                                        bt_compartilhar.setEnabled(false);
                                    }else{
                                        lb_arquivoTipo.setText(Avisos.TEXTO_ARQ_PRONTO_PARA_COMPART);
                                        bt_compartilhar.setEnabled(true);
                                    }
                                
                                //caso destinatario nao selecionado
                                }else{
                                    lb_arquivoTipo.setText(Avisos.TEXTO_SELECIONE_QUEM_BAIXA_ARQ);
                                    bt_compartilhar.setEnabled(false);
                                }
                                

                        }else{

                            System.out.println("Contem caracteres invalidos.");
                            MostrarDados.s_MostrarDados.mostraMensagemAtualizacao(Avisos.MSG_ARQ_CARACT_INV, "aviso");

                        }

                }

        }
}//GEN-LAST:event_bt_confirmarRenomearActionPerformed

    
    
    

    private void bt_aceitaCorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_aceitaCorActionPerformed
        try{

            //ATUALIZA COR NO BANCO
           String sqlUpdate = "UPDATE configuracoes_cores SET "+c_nomeCampoNoBanco+" = '"+c_cor+"' where cod_usuario = "+LogUsuario.s_cod;
           int mudou = auxiliar.Dados.s_conexaoBanco.c_statement.executeUpdate(sqlUpdate);

           //CASO ALTERADO...
           if(mudou == 1){

               //ATUALIZA CORES DO USUÁRIO
               Cores.s_Cores.atualizaCores(LogUsuario.s_cod, "usuario");

               //ATUALIZA CORES DA CONF DO USUÁRIO
               Cores.s_Cores.atualizaCores(LogUsuario.s_cod, "conf-usuario");


               dispose();
               s_telaPaletaEscolhaCor = null;
               System.out.println("tela de paleta escolha a cor fechada e null");
           }
        }
        catch(Exception e){
            System.err.println("Erro ao tentar atualizara cor no campo "+c_nomeCampoNoBanco+"!   \nErro:"+e);
        }
    }//GEN-LAST:event_bt_aceitaCorActionPerformed


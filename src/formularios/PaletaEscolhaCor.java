    private void seletor_coresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_seletor_coresMouseClicked
        bt_aceitaCor.setEnabled(false);
        //habilita botao de acitar
        bt_aceitaCor.setEnabled(true);

        //pega o valor red da cor selecionada
        c_r = seletor_cores.getColor().getRed();
        //pega o valor green da cor selecionada
        c_g = seletor_cores.getColor().getGreen();
        //pega o valor blue da cor selecionada
        c_b = seletor_cores.getColor().getBlue();

        //guarda o valr rgb na variável cor;
        c_cor = c_r+", "+c_g+", "+c_b;

            //caso o painel de fundo somente a cor do painel de teste mudará
            if(c_nomeCampoNoBanco.equals("cor_painel") || c_nomeCampoNoBanco.equals("cor_lista_recado") || c_nomeCampoNoBanco.equals("cor_lista_amigos"))
                jp_painelTeste.setBackground(new java.awt.Color(c_r, c_g, c_b));
            //caso seja algum label somente o label mudará de c_cor
            else
                lb_objeto.setForeground(new java.awt.Color(c_r, c_g, c_b));
}//GEN-LAST:event_seletor_coresMouseClicked

